def add(a,b):
    """
Adds two numbers together.

Args:
    a (int or float): The first number to be added.
    b (int or float): The second number to be added.

Returns:
    int or float: The sum of `a` and `b`.

Raises:
    TypeError: If either `a` or `b` is not an integer or float.

Example:
    >>> add(3, 5)
    8
"""
return a+b